// Assignment operators

// +=
let a = 3;
a += 4;
console.log("a = ",a)

// -=
let b = 5;
b -= 2;                   // b = b-2 
console.log("b = ",b)

// *=
let c = 3;
c *=3;                  // c = c*3
console.log("c = ",c)

// %=
let d = 4;
d %=2;                  // d = d%2
console.log("d = ",d)

// **=
let e = 5;
e **=2;                 // e = e**2
console.log("e = ",e)

// /=
let f = 6;
f /= 3;                 // f = f/3
console.log("f = ",f)